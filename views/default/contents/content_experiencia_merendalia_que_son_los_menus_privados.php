					<i><strong>A Mesa Puesta 
					
					</i></strong>
				</p>

				</br>
				<p>
					Vamos adaptando los menús a vuestros gustos o a los productos de
					temporada. De ahí que vayan cambiando. Si quieres que te preparemos
					algo en concreto, o alguien te ha recomendado algo que probó y
					ahora no lo encuentras entre nuestras opciones, pregúntanos.</br> </br>
					Nos gusta la cocina un montón, la nuestra y la de fuera, y por eso
					alguno de nuestros menús son internacionales. El menú alemán dentro
					de la fiesta OktoberFest, es uno de los que más éxito ha tenido y
					por eso lo mantenemos desde octubre. El menú japonés, es más
					reciente. Sabemos que lo japo triunfa y a veces no tanto, por el
					pescado crudo. Este menú gusta a unos y sorprende a los otros.
					Entre los nacionales destacar el menú sidrería, menú contundente,
					rico y festivo. Se sirve con sidra, con la ayuda de nuestros amigos
					“los isidrines”. Es un menú al que tenemos mucho cariño. Siempre
					nos gustó mucho visitar las sidrerías cuando viajábamos por el
					norte de España, y fue con ese menú con el que arrancamos esta
					aventura, en septiembre del año pasado, en las fiestas de
					Valladolid. Desde entonces son muchas las personas que lo han
					probado y uno de sus platos se ha convertido sin dudadarlo, en el
					plato estrella de merendalia. Hablamos del bacalao al pil pil.</br>
					</br> Nos gusta que se prueben varios platos, y por eso <strong>los
						menús son pequeñas degustaciones</strong>. Siempre hay unos
					cuantos, a compartir, para terminar con un principal. Españoles o
					internacionales, todos los menús, comparten el sabor. <strong>Las
						preparaciones son caseras</strong> y eso se nota. Ponemos mucho
					empeño en <strong>buscar los mejores proveedores</strong> que nos
					den los mejores productos. Un ejemplo de esto es la paliza que le
					di a Jose, nuestro carnicero, con los codillos, en pleno diseño del
					menú Oktober. Probamos con los traseros. Con piel, sin piel. Ahora
					dámelos delanteros y sin piel. Así hasta dar con la pieza que nos
					permitió elaborar el codillo perfecto, asado en su jugo al estilo
					Alemán. Un abrazo a Jose y Rocío!!

				</p>
