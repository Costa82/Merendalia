				<p>
					<i><strong>Bocaditos 
					
					</i></strong>
				</p>
				</br>

				<p>
					Esta segunda opción nace de vosotros. Sabemos que sentarse a la
					mesa es la mejor opción en muchas de vuestras reuniones, pero a
					veces, buscáis algo más informal. De pie, interactuando unos con
					otros y sin descuidar la cocina. Son como los ágapes de las bodas,
					un vino español, estar de pinchos, etc…, pero con todas las
					ventajas que ofrece merendalia. Tapear por el centro es un planazo,
					pero no siempre es lo más cómodo. Los abrigos estorban, tienes que
					andar cambiando de bar y muchas veces no se sabe que hacer con los
					niños.</br> </br> El plan ideal para organizar un buen vermú de fin
					de semana.</br> </br> Disponemos de tres menús diferentes a base de
					pinchos fríos, calientes y dulces. Vino, cerveza, agua o refresco
					incluido. </br> </br> </br> </br> Sea a mesa puesta o de bocaditos,
					sabemos que <strong>somos una de las mejores opciones de Valladolid
						para tus reuniones.</strong> ¿Estás esperando a tu próxima
					celebración? Pues no esperes más e invéntate un motivo. Ven con tu
					familia, con tus amigos, traeros a los niños. Se acabó el ir a unos
					sitios con los padres o hijos y a otros con los amigos… En
					merendalia hay hueco para todos.</br> </br>
				</p>
				<strong>Somos el primer restaurante completamente privado en el
					centro de Valladolid.</strong></br> </br> ¡Os esperamos! </br>


