			<div class='explicacion_final'>

				<p>
					<strong>¿En qué consisten las meriendas-taller?</strong>
				</p>
				</br>
				<p>Son sesiones de hora y media en la que los niños estarán
					acompañados por nuestros monitores. Serán grupos de máximo 10
					niños, por lo que tendrán un trato personalizado. Sabemos que cada
					personita requiere unas atenciones diferentes. Cada taller tendrá
					una temática central. El de este jueves giró en torno a LA MÚSICA.
					En él, aprendimos a hacer un instrumento con material reciclado y
					¡decorarlo y pintarlo como más le gustó a cada uno!, después, todo
					lo que hicieron, se lo llevaron a casa. También les enseñamos
					diferentes instrumentos de percusión de distintos lugares del
					mundo, y cantamos una canción con ellos. Hicimos un cuentacuentos
					interactivo, en el que cada uno de ellos era un personaje y cada
					vez que ese personaje salía en el cuento, ellos tenían que tocar el
					instrumento que habían elegido previamente. ¡He de decir que se lo
					pasaron pipa y enseguida se metieron en su papel! Como colofón a la
					jornada, merendamos, una comida sana y saludable, y como no,
					¡jugamos un poco en nuestro parque de bolas!, esto último como
					premio a lo bien que se habían portado en el taller. La verdad que
					los niños, durante el mismo, no se acordaron del parque, estuvieron
					muy atentos a la actividad y a sus amiguitos con los que
					compartieron esa hora y media.</p>
				</br>
				<p>Aquí os dejamos algunas fotos del taller y ¡os animamos a que
