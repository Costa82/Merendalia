	<div class='explicacion_articulo'>

		<p>A todos nos están pasando cosas que nos gustaría celebrar con los
			nuestros en estos meses raros, y estamos dejando pasar las fechas
			para ver si “volvemos a la normalidad”, pero la realidad es que ni
			sabemos si vamos a volver a la vida que teníamos antes, al menos de
			momento, ni el 2020 va a poder “ser” en 2021 (¡¡no hay tiempo
			material ni días en el calendario!!).</p>
		</br>
		<p>Además, ¡qué narices!:</p>
		<p>
			</br>&nbsp&nbsp&nbsp&nbsp- los 40, o los 18 o los 37, o los
			que sean, los haces este año, no el que viene </br></br>
			&nbsp&nbsp&nbsp&nbsp- el hijo de tu mejor amiga va a nacer este año,
			no el que viene </br></br> &nbsp&nbsp&nbsp&nbsp- las bodas de oro de tus
			padres son este año, no el que viene </br></br> &nbsp&nbsp&nbsp&nbsp- tu
			pareja hace 30 este año, y lo que más ilusión la haría sería una
			fiesta sorpresa, no el que viene </br></br> &nbsp&nbsp&nbsp&nbsp- te
			jubilas este año, no el que viene…</br>
		</p>
		</br><p>y un sinfín de acontecimientos que van ocurriendo en nuestras vidas
			y que no puedes dejar pasar, porque lo que pasa, se va, no vuelve. Y
			lo que no se celebra, tú ya lo sabes, es como si no hubiera pasado.</p>
		</br>
		<p>Como la seguridad no está reñida con la alegría y la celebración,
			vamos a celebrar nuestro evento con toda la seguridad, pero sin dejar
			que se pase ese día tan especial. Para ello te traemos una serie de
			consejos / tips que pueden ayudarte a que esto sea más fácil:</p>
			<p>
		</br>&nbsp&nbsp&nbsp&nbsp- <b>Tener un espacio exterior:</b> para que
		el que quiera estar fuera por sentirse así más seguro, pueda estarlo y
		aun así asistir a tu fiesta. </br></br> &nbsp&nbsp&nbsp&nbsp- <b>Explica a tus
		invitados que les invitas a un lugar privado en el que solo estaréis
		vosotros,</b> sin otros grupos de personas alrededor que puedan dar
		inseguridad. </br></br> &nbsp&nbsp&nbsp&nbsp- <b>Invita solo a las personas
		que sí o sí quieras que estén en tu celebración:</b> es hora de hacer
		criba en tus amistades y de no invitar a gente por “hacer bulto”,
		rodéate de las personas que realmente quieres a tu lado en ese día tan
		especial, e invítalas solo a ellas. De esta forma te aseguras no abrir
		demasiado el círculo de personas con las que mantienes contacto, pero
		a la vez no dejas de celebrar tu evento. </br></br> &nbsp&nbsp&nbsp&nbsp-
		<b>Puedes hacer mascarillas personalizadas de recordatorio.</b> Te servirá
		para dos cosas: la primera dar seguridad, y además dar un toque de
		humor a la situación de la pandemia, que las fotos que os hagáis sean
		más divertidas y que todo el mundo se sienta seguro, Muchas veces se
		hace un detalle de recordatorio que después no sirve para nada, por
		qué no hacer unas mascarillas graciosas y divertidas, con alguna frase
		o dibujo de la temática del evento. </br></br> &nbsp&nbsp&nbsp&nbsp- <b>Di a
		tus invitados que, si lo prefieren, pueden pasarse solo un rato,</b> habrá
		gente que quiera acompañarte en este día tan especial, pero solo un
		tiempo prudencial, se pueden pasar a tomar una cerveza, saludarte y
		luego irse. Y habrá otros que te acompañarán durante todo el evento.
		La manera de disfrutar de Merendalia hace que eso sea posible, ya que
		puedes disponer del espacio todo el tiempo que quieras, solo para
		vosotros.</p></br>

		</br><p>Con todas estas ideas puedes montar una buena celebración en la que
			os volveréis a ver todos, pasaréis un rato juntos, que falta nos
			hace, (¡¡¡en este confinamiento nos hemos echado mucho de
			menosssss!!!) y a la vez no habrás dejado que la fecha señalada pase
			sin pena ni gloria por el calendario. Las cosas hay que celebrarlas,
			y este año, más.</p>
