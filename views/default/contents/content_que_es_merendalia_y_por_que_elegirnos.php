				<p>
					<i>¿Qué opciones hay en la reserva?</i>
				</p>

				</br>
				<p>
					Queremos que las reuniones, sean del tipo que sean, giren en torno
					a la buena mesa. Y sobre esto hemos sido lo más flexibles posible.
					Puedes encargarnos la comida o traerla. Puedes traer unas cosas y
					encargarnos otras. O incluso ¡puedes disponer de la cocina y
					cocinar allí mismo!, una cocina completa te está esperando, con
					todos los electrodomésticos, menaje y demás que necesites, ¡el
					sueño de un cocinillas hecho realidad!¿Has oído hablar de los <strong>chocos
						vascos</strong>, o, más de nuestra tierra, <strong>el merendero o
						la bodega del pueblo?</strong> pues, para entendernos, es algo
					así, pero en el centro de la ciudad, sin necesidad de tener que
					coger el coche para volver a tu casa, bien comunicado con varias
					líneas de autobús y paradas de taxi cercanas, al lado de la
					Catedral de Valladolid, y, para los más festivos, al lado de un
					montón de bares y lugares donde poder seguir tu celebración una vez
					que salgas de merendalia.
				</p>
				</br>
				<p>
					<i>¿Qué nos puedes encargar?</i>
				</p>
				</br>

				<p>Desde una merienda de picoteo, tortillas, empanadas, embutidos,
					ensaladas..., hasta un menú completo. Has leído bien. Son nuestros
					menús “calentar y listo”. Se elaboran para que requieran la mínima
					intervención en el acto, de manera que se puedan dejar preparados.
					Estos menús incluyen, entrantes, primero, segundo, postre, café…
					Todo listo para que vosotros tengáis que hacer lo menos posible,
					pero a la vez, estéis como en casa, porque estaréis solos. Después,
					la limpieza corre a cargo de nosotros, así que… ¡solo os queda
					disfrutar!</p>
				</br>
				<p>Dependiendo del tipo de evento o del número de personas que
					seáis, disponemos las mesas de una forma u otra. Si lo que queréis
					es un picoteo de estar de pie para interactuar más unos con otros,
					dejamos las mesas pegadas a la pared. Si optáis por un menú
					completo, os dejamos la mesa puesta para el número que seáis.</p>
				</br>
				<p>
					<i>¿Por qué reservar? ¿Cuáles son las ventajas?</i>
				</p>
				</br>
				<p>Las casas son cada vez más pequeñas y cuesta juntarse. Organizar
					algo en casa implica comprar, cocinar, preparar la mesa y luego
					recoger y limpiar. En merendalia nos podemos encargar de todo, si
					así lo deseas. No necesitas coger el coche. Sabemos que quien más
					quien menos tiene casa en el pueblo o un amigo con bodega. Estamos
					en el centro para que no tengas que desplazarte. Disponemos de
					parque de bolas, tronas, cambiador de bebés, y el local está
					adaptado a personas de movilidad reducida.</p>
				</br> </br>

				<p>
					<u>Menús Privados 
				
				</p>
				</u> </br>
				<p>
					<i>¿En qué consisten?</i>
				</p>
				</br>

				<p>Son banquetes. Menús que se sirven a la fecha y hora que se elige
					y para un mínimo de 12 personas. Estáis vosotros solos y el
					personal que os cocina y sirve.</p>
				</br>
				<p>
					<i>¿Por qué organizar un menú privado?</i>
				</p>
				</br>

				<p>
					Porque tienes que probarlo. Es ir a un restaurante, pero estando
					como en casa. Nadie te molesta y tampoco molestas a nadie. El
					parque de bolas permite a los padres tener una sobremesa tranquila
					mientras los niños juegan. </br> </br> Y lo más importante, por la
					cocina. Nuestros menús están elaborados buscando el sabor, lo que
					se prepara está rico de verdad y siempre es casero. Intentamos
					además que tengan varios platos, para que así sea una degustación.
					Nos gusta mucho mimaros, desde el principio hasta el final de
					vuestro evento, y el principio está en el producto y en la cocina.
				</p>
				</br>
				<p>
					<u>Los eventos:</u>
				</p>
				</br>

				<p>
					Son nuestras propuestas para días concretos. Los hemos hecho
					gastronómicos con motivo del Oktober, la vendimia, catas de vino y
					jamón, etc. Ahora, como os presentamos la semana pasada, las <strong>meriendas-taller</strong>
					todos los jueves después del cole. Y próximamente, y esto es un
					avance, el <strong>primer concurso de humor libre de merendalia</strong>.
				</p>
				</br>
				<p>Por último queremos hacer énfasis en que siempre nos acordamos de
					los más pequeños de la casa, con nuestro parque de bolas al fondo
					de la sala, nuestras tronas para que les des de comer lo más cómodo
					posible, nuestro cambiador de bebés, siempre listo y limpio para
					que, una vez más, no notéis que estáis fuera de casa… en
					definitiva, este es el espacio que os hemos preparado para vuestro
					disfrute y que cada día tratamos con mimo y cariño, tanto en su
					preparación de cara a vuestro evento, como en su limpieza,
					organización, etc.</p>
				</br>
				<p>En futuras entradas iremos desgranando más a fondo nuestras
					propuestas con fotos de todo. Hace tiempo que queríamos explicar un
					poco más lo que hacemos y lo que se puede hacer y esperamos que
					estas líneas os hayan ayudado. Aquí os esperamos, ¡¡para dar forma
					a vuestro ocio!!</p>
				</br>
				<p>
					<strong>Merendalia, el ocio a tu manera.</strong>
				</p>

